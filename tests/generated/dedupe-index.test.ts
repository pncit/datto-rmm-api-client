import { describe, test, expect } from "vitest";

import { dedupeExportLines } from "../../scripts/dedupe-generated-index.mjs";

describe("dedupeExportLines", () => {
  test("removes a duplicate export pair that differs only by a trailing .js extension", () => {
    const content = [
      "export * from './foo';",
      "export * from './foo.js';",
      "export * from './bar';",
    ].join("\n");

    const { content: deduped, duplicatesRemoved } = dedupeExportLines(content);

    expect(duplicatesRemoved).toBe(1);
    expect(deduped).toBe(
      ["export * from './foo';", "export * from './bar';"].join("\n"),
    );
  });

  test("preserves non-export lines (comments, blank lines) untouched", () => {
    const content = [
      "/**",
      " * Generated by orval",
      " */",
      "",
      "export * from './foo';",
      "export * from './foo';",
    ].join("\n");

    const { content: deduped, duplicatesRemoved } = dedupeExportLines(content);

    expect(duplicatesRemoved).toBe(1);
    expect(deduped).toBe(
      [
        "/**",
        " * Generated by orval",
        " */",
        "",
        "export * from './foo';",
      ].join("\n"),
    );
  });

  test("is idempotent: a second pass over already-deduped content removes nothing", () => {
    const content = [
      "export * from './foo';",
      "export * from './foo.js';",
    ].join("\n");

    const first = dedupeExportLines(content);
    expect(first.duplicatesRemoved).toBe(1);

    const second = dedupeExportLines(first.content);
    expect(second.duplicatesRemoved).toBe(0);
    expect(second.content).toBe(first.content);
  });

  test("reports zero duplicates removed when there are none", () => {
    const content = ["export * from './foo';", "export * from './bar';"].join(
      "\n",
    );

    const { content: deduped, duplicatesRemoved } = dedupeExportLines(content);

    expect(duplicatesRemoved).toBe(0);
    expect(deduped).toBe(content);
  });
});
